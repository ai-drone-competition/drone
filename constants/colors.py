"""
공통 색상 정의 모듈
"""

# 색상 코드 정의 (ANSI 색상)
class Colors:
    GREEN = '\033[92m'    # 성공
    BLUE = '\033[94m'     # 정보
    YELLOW = '\033[93m'   # 경고
    RED = '\033[91m'      # 에러
    PURPLE = '\033[95m'   # 강조
    CYAN = '\033[96m'     # 진행상황
    BOLD = '\033[1m'      # 굵게
    END = '\033[0m'       # 색상 초기화
